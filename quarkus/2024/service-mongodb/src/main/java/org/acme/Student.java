//@MongoEntity(collection = "Student")
@MongoEntity
public class Student extends PanacheMongoEntity {
    public String name;
    public int marks;


    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getMarks() {
        return marks;
    }

    public void setMarks(int marks) {
        this.marks = marks;
    }
}
